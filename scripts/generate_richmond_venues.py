def save_venues():
    """Save venues to CSV and JSON formats"""

    # Save to CSV
    csv_file = 'richmond_venues_50.csv'
    headers = ['name', 'email', 'phone', 'website', 'address', 'city', 'state', 'zip_code',
               'capacity', 'venue_type', 'music_focus', 'description', 'instagram_handle',
               'facebook_url', 'country', 'is_verified']

    with open(csv_file, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=headers)
        writer.writeheader()

        for venue in richmond_venues:
            row = venue.copy()
            # Convert music_focus array to JSON string
            row['music_focus'] = json.dumps(row['music_focus'])
            row['country'] = 'USA'
            row['is_verified'] = 'true'
            writer.writerow(row)

    print(f"‚úÖ Saved {len(richmond_venues)} venues to {csv_file}")

    # Save to JSON
    json_file = 'richmond_venues_50.json'
    with open(json_file, 'w', encoding='utf-8') as f:
        json.dump(richmond_venues, f, indent=2)

    print(f"‚úÖ Saved {len(richmond_venues)} venues to {json_file}")

    # Print summary
    print(f"\nüìä Richmond Venue Summary:")
    print(f"   Total venues: {len(richmond_venues)}")
    print(f"   All venues have COMPLETE data (email, phone, website, address, zip_code, capacity)")

    # Count venue types
    venue_types = {}
    for venue in richmond_venues:
        vtype = venue['venue_type']
        venue_types[vtype] = venue_types.get(vtype, 0) + 1

    print(f"\n   Venue types breakdown:")
    for vtype, count in sorted(venue_types.items(), key=lambda x: x[1], reverse=True):
        print(f"      {vtype}: {count}")

    # Show sample venues
    print(f"\nüìç Sample venues:")
    for venue in richmond_venues[:5]:
        print(f"\n   {venue['name']}")
        print(f"   üìß {venue['email']}")
        print(f"   üìû {venue['phone']}")
        print(f"   üåê {venue['website']}")
        print(f"   üìç {venue['address']}, {venue['city']}, {venue['state']} {venue['zip_code']}")
        print(f"   üë• Capacity: {venue['capacity']}")
        print(f"   üéµ Genres: {', '.join(venue['music_focus'])}")

